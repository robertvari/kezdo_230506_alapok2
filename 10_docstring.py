def print_user_data(name: str, address: str, email:str, age:int):
    """
    This function prints the user data in a readable format.
    name = str
    address = str
    email = str
    age = int
    """

    print(f"-"*20)
    print(f"Name: {name}")
    print(f"Address: {address}")
    print(f"Email: {email}")
    print(f"Age: {age}")
    print(f"-"*20)